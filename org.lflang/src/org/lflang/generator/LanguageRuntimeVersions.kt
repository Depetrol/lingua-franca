package org.lflang.generator

import java.io.FileNotFoundException
import java.util.*

/** Mediates access to the persisted Git revisions of the language runtime submodules. */
object LanguageRuntimeVersions {

    // This file is generated by gradle.
    private const val RUNTIME_VERSIONS_FILE = "org/lflang/generator/runtime-versions.properties"

    private val runtimeVersionProperties by lazy {
        val stream = Thread.currentThread().contextClassLoader.getResourceAsStream(RUNTIME_VERSIONS_FILE)
            ?: throw FileNotFoundException("Cannot find $RUNTIME_VERSIONS_FILE")
        Properties().apply { load(stream) }
    }

    /** The committed version of the Rust runtime. */
    val rustRuntimeVersion: String?
        get() =
            runtimeVersionProperties["rs"]?.toString()

}
